#include "main.h"
/**
 * _abs - the entry point
 * Description: the function prints absolute values
 * @int: data type integer as input
 * Return: 0
 */
int _abs(int)
{
	int n;

	if (n > 0)
	{
		_putchar(n);
	}
	else if (n == 0)
	{
		_putchar(n);
	}
	else
	{
		_putchar(('n * -1') + '0');
	}
	return (0);
}
